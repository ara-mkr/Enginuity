const DB_NAME = 'enginguity_versions'
const DB_VERSION = 1

function openDB() {
  return new Promise((resolve, reject) => {
    const request = indexedDB.open(DB_NAME, DB_VERSION)

    request.onupgradeneeded = (e) => {
      const db = e.target.result

      if (!db.objectStoreNames.contains('snapshots')) {
        const store = db.createObjectStore('snapshots', { keyPath: 'id' })
        store.createIndex('moduleId', 'moduleId', { unique: false })
        store.createIndex('timestamp', 'timestamp', { unique: false })
        store.createIndex('moduleTimestamp', ['moduleId', 'timestamp'], { unique: false })
      }
    }

    request.onsuccess = (e) => resolve(e.target.result)
    request.onerror = (e) => reject(e.target.error)
  })
}

export async function saveSnapshot(snapshot) {
  const db = await openDB()
  return new Promise((resolve, reject) => {
    const tx = db.transaction('snapshots', 'readwrite')
    const req = tx.objectStore('snapshots').put(snapshot)
    req.onsuccess = () => resolve()
    req.onerror = (e) => reject(e.target.error)
    tx.oncomplete = () => pruneOldSnapshots(db, snapshot.moduleId)
  })
}

export async function getSnapshots(moduleId, limit = 50) {
  const db = await openDB()
  return new Promise((resolve, reject) => {
    const tx = db.transaction('snapshots', 'readonly')
    const index = tx.objectStore('snapshots').index('moduleId')
    const request = index.getAll(moduleId)
    request.onsuccess = () => {
      const results = request.result
        .sort((a, b) => b.timestamp - a.timestamp)
        .slice(0, limit)
      resolve(results)
    }
    request.onerror = (e) => reject(e.target.error)
  })
}

export async function getAllSnapshots() {
  const db = await openDB()
  return new Promise((resolve, reject) => {
    const tx = db.transaction('snapshots', 'readonly')
    const request = tx.objectStore('snapshots').getAll()
    request.onsuccess = () => {
      resolve(request.result.sort((a, b) => b.timestamp - a.timestamp))
    }
    request.onerror = (e) => reject(e.target.error)
  })
}

export async function getSnapshot(id) {
  const db = await openDB()
  return new Promise((resolve, reject) => {
    const tx = db.transaction('snapshots', 'readonly')
    const req = tx.objectStore('snapshots').get(id)
    req.onsuccess = () => resolve(req.result)
    req.onerror = (e) => reject(e.target.error)
  })
}

export async function updateSnapshot(id, updates) {
  const db = await openDB()
  return new Promise(async (resolve, reject) => {
    const snap = await getSnapshot(id)
    if (!snap) return reject(new Error('Snapshot not found'))
    const tx = db.transaction('snapshots', 'readwrite')
    const req = tx.objectStore('snapshots').put({ ...snap, ...updates })
    req.onsuccess = () => resolve()
    req.onerror = (e) => reject(e.target.error)
  })
}

export async function deleteSnapshot(id) {
  const db = await openDB()
  return new Promise((resolve, reject) => {
    const tx = db.transaction('snapshots', 'readwrite')
    const req = tx.objectStore('snapshots').delete(id)
    req.onsuccess = () => resolve()
    req.onerror = (e) => reject(e.target.error)
  })
}

export async function clearAutoSnapshots(moduleId) {
  const db = await openDB()
  const snaps = await (moduleId ? getSnapshots(moduleId, 10000) : getAllSnapshots())
  const toDelete = snaps.filter(s => s.autoGenerated)
  return new Promise((resolve, reject) => {
    const tx = db.transaction('snapshots', 'readwrite')
    const store = tx.objectStore('snapshots')
    for (const snap of toDelete) store.delete(snap.id)
    tx.oncomplete = () => resolve(toDelete.length)
    tx.onerror = (e) => reject(e.target.error)
  })
}

async function pruneOldSnapshots(db, moduleId, keepCount = 100) {
  return new Promise((resolve, reject) => {
    const tx = db.transaction('snapshots', 'readonly')
    const index = tx.objectStore('snapshots').index('moduleId')
    const req = index.getAll(moduleId)
    req.onsuccess = () => {
      const all = req.result.sort((a, b) => b.timestamp - a.timestamp)
      if (all.length <= keepCount) return resolve()

      // Only prune auto-generated, keep named ones regardless
      const autoSnaps = all.filter(s => s.autoGenerated)
      const namedSnaps = all.filter(s => !s.autoGenerated)
      const totalToKeep = Math.max(0, keepCount - namedSnaps.length)
      const toDelete = autoSnaps.slice(totalToKeep)

      if (toDelete.length === 0) return resolve()

      const writeTx = db.transaction('snapshots', 'readwrite')
      const store = writeTx.objectStore('snapshots')
      for (const snap of toDelete) store.delete(snap.id)
      writeTx.oncomplete = () => resolve()
      writeTx.onerror = (e) => reject(e.target.error)
    }
    req.onerror = (e) => reject(e.target.error)
  })
}
